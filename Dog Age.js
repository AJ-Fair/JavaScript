// This variable is my age. //
const myAge = 29;
let earlyYears = 2;
earlyYears *= 10.5;
// This variable is calculated by subtracting two from myAge. //
let laterYears = myAge - 2;
// This line multiplies laterYears by four and reassigns it a new value. //
laterYears *= 4;
// This line calculates my age in dog years. //
let myAgeInDogYears = earlyYears + laterYears;
// This line is my name. //
const myName = 'AJ Fair'.toLowerCase();
// This line prints out the results of my code. //
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)